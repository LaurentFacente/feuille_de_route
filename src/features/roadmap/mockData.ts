function cat(id: string, name: string, items: [string, boolean?][]): ChecklistCategory {
  return {
    id,
    name,
    items: items.map(([label, checked], i) => ({
      id: `${id}-${i}`,
      label,
      checked: Boolean(checked),
    })),
  }
}

