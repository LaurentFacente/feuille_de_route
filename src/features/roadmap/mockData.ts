const at = (date: string, hm: string) => `${date}T${hm}:00`

interface StepSeed {
  id: string
  title: string
  phase: string
  start: string
  end: string
  location?: string
  participants?: string[]
  equipment?: string[]
  vehicles?: string[]
  details?: string[]
}

function step(seed: StepSeed): Step {
  return {
    id: seed.id,
    title: seed.title,
    phase: seed.phase,
    start: seed.start,
    end: seed.end,
    location: seed.location,
    participants: seed.participants ?? [],
    equipment: seed.equipment ?? [],
    vehicles: seed.vehicles ?? [],
    shots: [],
    details: seed.details ?? [],
    override: 'auto',
    shiftMinutes: 0,
    comments: [],
  }
}

